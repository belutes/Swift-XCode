//: Playground

import UIKit

// This is a comment

var str = "Hello, playground"

print(str)

str = "Another playground"

print(str)

// Constants
// Constants use keyword "let"
let con = "Yet another playground"
// The value of a constant cannot be changed
print(con)

// Boolean values are either true or false
var b = false // Notice not a string, no ""
print(b)

// Integers are whole numbers, no decimals, can be pos or neg.
var i = 32
i = 0
i = -23
print(i)

// floating point numbers (double) are
// fractional (contain decimal points)
var f:Float = 29.398
f = -0.23
print(f)

